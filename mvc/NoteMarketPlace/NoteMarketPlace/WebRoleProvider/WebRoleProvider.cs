        public override string[] GetRolesForUser(string username)
        {
            using (var _context =  new NotesMarketPlaceEntities())
            {
                var result = (from user in _context.tblUsers
                              join role in _context.tblUserRoles on user.RoleID equals role.ID
                              where user.EmailID == username
                              select role.Name).ToArray();

                return result;
            }
        }

